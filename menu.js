import React from "react";
import Announcement from "components/announcement/announcement";

/**
 * TODO: RENAME TO ANNOUNCEMENTS
 */
const AnnouncementList = ({ announcement, dict }) => {
  const listItems = announcement.map((announcementItem, index) => (
    <Announcement
      key={index}
      title={announcementItem.title}
      date={announcementItem.as_of}
      body={announcementItem.body}
    />
  ));
  return (
    <div className="news-wrapper">
      <h3>{dict["Recent_Announcements"]}</h3>
      <ul>{listItems}</ul>
    </div>
  );
};

export default AnnouncementList;
